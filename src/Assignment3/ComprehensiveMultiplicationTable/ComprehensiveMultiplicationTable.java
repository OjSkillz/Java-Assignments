
public class ComprehensiveMultiplicationTable {
  public static void main(String[] args) {
  
 
  for (int row = 1; row <= 9; row++) {
      for (int multiplier = 1; multiplier <=9; multiplier++) {
      System.out.print(row + " * " + multiplier + " = " + row * multiplier + 
      "   \t"); 
      } System.out.println();
    }
  }
}
